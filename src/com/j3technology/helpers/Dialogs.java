package com.j3technology.helpers;

import javafx.scene.control.Alert;
import javafx.scene.control.Alert.AlertType;
import javafx.stage.StageStyle;

/**
 *
 * @author Ganraj
 */
public class Dialogs {
    public static void success(String msg){
        Alert alert = new Alert(AlertType.INFORMATION);
        alert.setTitle("Information Dialog");
        alert.setHeaderText(null);
        alert.setContentText(msg);
        alert.initStyle(StageStyle.TRANSPARENT);
        alert.getDialogPane().setStyle("-fx-background-color: linear-gradient(to top right, rgba(0, 255, 127, 0.4), royalblue);\n" +
        "-fx-effect: dropshadow( three-pass-box , rgba(0,0,0,0.6) , 5, 0.0 , 0 , 1 );-fx-fill: white;\n" +
        "-fx-font-size: 14px;-fx-font-weight: bold;");
        alert.showAndWait();
    }
    public static void warning(String msg){
        Alert alert = new Alert(AlertType.WARNING);
        alert.setTitle("Information Dialog");
        alert.setHeaderText(null);
        alert.setContentText(msg);
        alert.initStyle(StageStyle.TRANSPARENT);
        alert.getDialogPane().setStyle("-fx-background-color: linear-gradient(to top right, rgba(0, 255, 127, 0.4), royalblue);\n" +
        "-fx-effect: dropshadow( three-pass-box , rgba(0,0,0,0.6) , 5, 0.0 , 0 , 1 );-fx-fill: white;\n" +
        "-fx-font-size: 14px;-fx-font-weight: bold;");
        alert.showAndWait();
    }
}
